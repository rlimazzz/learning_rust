fn main() {
    let mut dias = 1;
    while dias <= 5 {
        if dias == 1 {
            println!("On the First day of christmas, my true love sent to me");
        }
        else if dias == 2 {
            println!("On the Second day of christmas, my true love sent to me");
        }
        else if dias == 3 {
            println!("On the third day of christmas, my true love sent to me");
        }
        else if dias == 4 {
            println!("On the fourth day of christmas, my true love sent to me");
        }else {
            println!("On the fifth day of christmas, my true love sent to me");
        }
        dias += 1;
    }
}
